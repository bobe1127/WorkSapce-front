console.log(`Math.min(1, 2, 3) = ${Math.min(1, 2, 3)}`);
console.log(`Math.max(1, 2, 3) = ${Math.max(1, 2, 3)}`);
console.log(`Math.abs(-1) = ${Math.abs(-1)}`);
console.log(`Math.pow(2, 3) = ${Math.pow(2, 3)}`);
// ceil
console.log(`Math.ceil(1.2) = ${Math.ceil(1.2)}`);
console.log(`Math.ceil(1.8) = ${Math.ceil(1.8)}`);
// floor
console.log(`Math.floor(1.2) = ${Math.floor(1.2)}`);
console.log(`Math.floor(1.8) = ${Math.floor(1.8)}`);
// round
console.log(`Math.round(1.2) = ${Math.round(1.2)}`);
console.log(`Math.round(1.8) = ${Math.round(1.8)}`);
// random
console.log(`Math.random() = ${Math.random()}`);
// 10个数, 1开始, 1-10
console.log(`Math.random() = ${Math.floor(Math.random() * 10) + 1}`);
// 10个数, 0开始, 0-9
console.log(`Math.random() = ${Math.floor(Math.random() * 10)}`);
